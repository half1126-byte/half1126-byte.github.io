          <details class="faq-item">
            <summary data-i18n="tx.ortho.q2">투명교정(인비절라인)은 모든 경우에 가능한가요?</summary>
            <div class="faq-answer" data-i18n="tx.ortho.a2">구강 상태에 따라 적용 가능 여부가 다릅니다. 진단 후 적합한 방법을 안내해 드립니다.</div>
