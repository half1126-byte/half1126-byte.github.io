    <section class="section section--band" id="faq" aria-labelledby="tx-faq">
      <div class="container">
        <h2 id="tx-faq" data-i18n="tx.sec.faq">자주 묻는 질문</h2>
        <div class="faq-list">
          <details class="faq-item">
            <summary data-i18n="tx.general.q1">어떤 진료가 포함되나요?</summary>
            <div class="faq-answer">충치 치료(레진·인레이), 신경(근관) 치료, 잇몸 치료(스케일링·치주 치료), 발치 등 구강 건강의 기본 진료를 포함합니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.general.q2">통증이 없어도 내원해야 하나요?</summary>
            <div class="faq-answer">충치나 잇몸 문제는 통증이 없는 초기에 발견·처치하면 훨씬 간단합니다. 정기 검진으로 일찍 확인하는 것을 권합니다.</div>
          </details>
          <details class="faq-item">
            <summary>신경 치료를 하면 치아를 뽑아야 하나요?</summary>
            <div class="faq-answer">신경 치료는 치아를 살리기 위한 치료입니다. 치아 내부 감염을 제거하고 보존하는 방향을 먼저 검토합니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.general.q3">치료 전 비용을 알 수 있나요?</summary>
            <div class="faq-answer" data-i18n="tx.general.a3">진단 후 치료 계획과 함께 비용을 투명하게 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary>사랑니도 발치하나요?</summary>
            <div class="faq-answer">사랑니 발치 여부는 위치·매복 형태·주변 치아 영향 등을 진단 후 안내합니다. 꼭 필요한 경우에만 신중하게 권합니다.</div>
          </details>
        </div>
        <p class="tx-note" data-i18n="tx.note">결과는 개인의 구강 상태에 따라 차이가 있을 수 있습니다.</p>
      </div>
    </section>

