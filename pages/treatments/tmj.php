        <h2 id="tx-faq" data-i18n="tx.sec.faq">자주 묻는 질문</h2>
        <div class="faq-list">
          <details class="faq-item">
            <summary data-i18n="tx.tmj.q1">턱에서 소리가 나면 치료가 필요한가요?</summary>
            <div class="faq-answer" data-i18n="tx.tmj.a1">소리만 있고 통증이나 기능 장애가 없는 경우 관찰이 적합할 수 있습니다. 진단 후 현재 상태와 관리 필요 여부를 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.tmj.q2">이갈이도 진료하나요?</summary>
            <div class="faq-answer" data-i18n="tx.tmj.a2">이갈이·이악물기는 턱관절과 치아 마모에 영향을 줄 수 있습니다. 원인을 파악하고 장치나 생활 관리로 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary>스플린트 장치는 어떻게 착용하나요?</summary>
            <div class="faq-answer">주로 수면 중 착용하는 장치로, 개인의 교합에 맞게 제작합니다. 착용 기간·방법·관리법을 상세히 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.tmj.q3">수면 관련 증상도 상담할 수 있나요?</summary>
            <div class="faq-answer" data-i18n="tx.tmj.a3">수면 중 이갈이, 코골이 관련 증상은 턱관절과 연관이 있을 수 있습니다. 진단 후 안내하며, 필요 시 협진을 권해 드릴 수 있습니다.</div>
          </details>
          <details class="faq-item">
            <summary>두통이 턱관절 때문일 수도 있나요?</summary>
            <div class="faq-answer">턱관절 주변 근육 긴장이 두통으로 이어지는 경우가 있습니다. 관련 증상이 있다면 함께 살피고 원인을 파악합니다.</div>
          </details>
        </div>
