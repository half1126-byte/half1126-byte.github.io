    <section class="section section--band" id="faq" aria-labelledby="tx-faq">
      <div class="container">
        <h2 id="tx-faq" data-i18n="tx.sec.faq">자주 묻는 질문</h2>
        <div class="faq-list">
          <details class="faq-item">
            <summary data-i18n="tx.prevention.q1">스케일링은 얼마나 자주 받나요?</summary>
            <div class="faq-answer">개인의 구강 상태에 따라 다릅니다. 일반적으로 6개월~1년 주기를 권하지만, 잇몸 상태에 따라 더 자주 필요한 경우도 있습니다. 검진 후 적합한 주기를 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary>GBT 스케일링은 일반 스케일링과 어떻게 다른가요?</summary>
            <div class="faq-answer">디스클로징 약제로 치태를 먼저 시각화하고, 에어플로우로 세심하게 제거합니다. 치면에 가해지는 자극을 줄이면서 관리하는 방식을 지향합니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.prevention.q2">YIDO Total Care는 무엇인가요?</summary>
            <div class="faq-answer" data-i18n="faq.a4">검진·스케일링부터 부종 케어, 예방관리, 구강관리 파우치까지 이어지는 환자경험 프로그램입니다. 세부 구성은 개원 시 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.prevention.q3">어린이도 예방 관리가 가능한가요?</summary>
            <div class="faq-answer">연령과 구강 상태에 맞춰 안내해 드립니다. 어린 시절부터의 정기 관리가 구강 건강의 기반이 됩니다.</div>
          </details>
          <details class="faq-item">
            <summary>잇몸 치료 후 관리가 필요한가요?</summary>
            <div class="faq-answer">잇몸 치료 후에도 정기적인 유지 관리가 중요합니다. 리콜 주기를 정하고 함께 관리합니다.</div>
          </details>
        </div>
        <p class="tx-note" data-i18n="tx.note">결과는 개인의 구강 상태에 따라 차이가 있을 수 있습니다.</p>
      </div>
    </section>

