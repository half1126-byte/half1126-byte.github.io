    <section class="section section--band" id="faq" aria-labelledby="tx-faq">
      <div class="container">
        <h2 id="tx-faq" data-i18n="tx.sec.faq">자주 묻는 질문</h2>
        <div class="faq-list">
          <details class="faq-item">
            <summary data-i18n="tx.aesthetic.q1">보철(심미) 치료는 어떻게 만들어지나요?</summary>
            <div class="faq-answer" data-i18n="faq.a6">의료진과 심미보철기공팀이 하나의 팀으로 움직입니다. 진단부터 색과 형태 조율까지 한 분의 보철을 함께 설계하고 다듬는 것을 지향합니다.</div>
          </details>
          <details class="faq-item">
            <summary>라미네이트와 크라운, 어떻게 다른가요?</summary>
            <div class="faq-answer">라미네이트는 치아 앞면에만 얇게 덮는 방식으로 삭제량이 적습니다. 크라운은 치아 전체를 감싸는 방식으로 변색·파절이 심한 경우에 선택합니다. 치아 상태와 목표에 따라 적합한 방법을 진단 후 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.aesthetic.q3">자연스러운 색을 맞출 수 있나요?</summary>
            <div class="faq-answer" data-i18n="tx.aesthetic.a3">원내 기공팀과 직접 색·형태를 조율해 자연스러움을 지향합니다. 결과는 개인의 치아 색과 구강 상태에 따라 차이가 있을 수 있습니다.</div>
          </details>
          <details class="faq-item">
            <summary>삭제는 얼마나 하나요?</summary>
            <div class="faq-answer">최소 삭제를 원칙으로 하며, 보철 방식과 치아 상태에 따라 달라집니다. 진단 전 삭제 범위 예상을 안내해 드립니다.</div>
          </details>
          <details class="faq-item">
            <summary data-i18n="tx.aesthetic.q2">어떤 보철 방식이 있나요?</summary>
            <div class="faq-answer" data-i18n="tx.aesthetic.a2">구강 상태와 목표에 따라 라미네이트·크라운·인레이·온레이 등 적합한 방식을 진단 후 안내해 드립니다.</div>
          </details>
        </div>
        <p class="tx-note" data-i18n="tx.note">결과는 개인의 구강 상태에 따라 차이가 있을 수 있습니다.</p>
